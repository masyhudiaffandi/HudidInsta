    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'user_id' => 'required',
            'post_Id' => 'required',
            'content' => 'required'
        ]);

        $comment = new Comment([
            'user_id' => $request->user_id,
            'post_id' => $request->post_Id,
            'content' => $request->content
        ]);

        $comment->save();

        return back()->with('success', 'Comment Added Successfully');
    }

